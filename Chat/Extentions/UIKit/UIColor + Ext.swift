import Foundation
import UIKit

extension UIColor {
    static func buttonRed() -> UIColor {
        return #colorLiteral(red: 1, green: 0, blue: 0.45329386, alpha: 1)
    }
    static func buttonBlack() -> UIColor {
        return #colorLiteral(red: 0, green: 0, blue: 0, alpha: 1)
    }
    static func mainWhite()-> UIColor {
        return #colorLiteral(red: 0.921431005, green: 0.9214526415, blue: 0.9214410186, alpha: 1)
    }
    
    
}
